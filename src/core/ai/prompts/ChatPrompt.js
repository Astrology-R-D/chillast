function nowString() {
  const now = new Date();
  const pad = (n) => String(n).padStart(2, '0');
  const week = '日一二三四五六'[now.getDay()];
  return `${now.getFullYear()}-${pad(now.getMonth() + 1)}-${pad(now.getDate())} ${pad(now.getHours())}:${pad(now.getMinutes())} 星期${week}`;
}

